						Après avoir traversé la période difficile de la pandémie de COVID-19, nous avons entrepris des réformes majeures pour réorganiser les activités du campus et réintégrer progressivement les étudiants.

						Le 17 février 2022, une réunion a été tenue dans la salle de conférence des employés de l'Église, réunissant des frères dynamiques désireux de collaborer pour faire progresser le campus. Des discussions ouvertes et productives, accompagnées d'un esprit de prière, ont conduit à l'adoption de certaines stratégies qui ont eu un impact positif sur le campus.

						En raison des nombreuses autres responsabilités qui exigent l'attention du coordinateur des Séminaires et Instituts de Religion, et dans le but de coordonner les efforts des classes du Séminaire et de l'Institut de Religion tout en supervisant les activités du campus, une équipe de superviseurs a été mise en place.<br/><br/><br/>
					
