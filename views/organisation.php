						<p>
							Le Campus Universitaire de l'Institut de Religion a été créé en avril 2019, sous l'impulsion du directeur inter-régional des Séminaires et Instituts de Religion d'Afrique Centrale. Au cours de cette année, la responsabilité a été confiée au coordinateur des Séminaires et Instituts de Religion pour identifier les jeunes adultes universitaires de toutes les unités des deux pieux de Brazzaville à cette époque.

							Une liste d'environ 100 étudiants universitaires issus des différentes unités du pieu de Brazzaville a été dressée et soumise au bureau régional des Séminaires et Instituts de Religion. Après l'approbation du campus universitaire de l'Institut de Religion pour Brazzaville, des réunions ont été tenues et des annonces ont été diffusées pour assurer que les dirigeants de la prêtrise de ces unités et les étudiants universitaires comprennent les objectifs du campus.

							Les étudiants disposeraient d'un lieu proche de leur université pour étudier l'Évangile de Jésus-Christ et partager des expériences enrichissantes pour leur foi. Un programme d'étude de l'Évangile spécialement conçu a été mis en place, prenant en compte leurs horaires scolaires.

							Le 17 juin 2019, un contrat de location a été signé pour un local à proximité de l'Université Marien Ngouabi de Brazzaville, avec l'aide du Département Immobilier de l'Église de Jésus-Christ des Saints des Derniers Jours.

							En juillet 2019, lors de sa visite de travail à Brazzaville, le directeur inter-régional des Séminaires et Instituts d'Afrique Centrale a tenu une réunion avec le coordinateur du campus et son assistant administratif pour donner des instructions sur le fonctionnement du campus. Au cours de cette réunion, il a demandé au coordinateur de soumettre un programme d'activités pour le campus au bureau des Séminaires et Instituts de Religion.

							Entre août et septembre 2019, le campus universitaire de l'Institut de Religion a ouvert ses portes, et à cette période, le cours de l'Institut de Religion était la seule activité proposée sur le campus. La participation a été progressive, avec entre 100 et 150 étudiants accueillis chaque semaine, parmi les membres et les non-membres de l'Église de Jésus-Christ des Saints des Derniers Jours.
						</p>
						
