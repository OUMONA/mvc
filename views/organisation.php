						<p>
							En raison de la capacité limitée de la salle de cours, le coordonnateur des Séminaires et Instituts de Religion a prévu trois cours d'Institut de Religion par jour, afin de permettre à tous les étudiants de participer confortablement. Par la suite, le programme EnglishConnect, axé sur l'apprentissage de la langue anglaise, ainsi qu'une connexion Internet, ont été introduits, ce qui a considérablement augmenté la participation aux cours d'Institut de Religion et à EnglishConnect. Environ 250 étudiants se sont inscrits et participent désormais au CAMPUS, la plupart d'entre eux n'étant pas membres de l'Église.

							Malheureusement, la pandémie de COVID-19 a eu un impact négatif sur les activités du campus. Entre décembre 2019 et mars 2020, les activités ont progressivement ralenti jusqu'à la fermeture, en raison des mesures de confinement liées aux précautions contre la COVID-19. Bien que le campus ait prévu d'enseigner les cours de l'Institut de Religion en ligne, il n'a pas été possible de le faire en raison du grand nombre d'étudiants n'ayant pas un accès facile à Internet.

							Lorsque certaines restrictions ont été assouplies et que le gouvernement a fixé un maximum de 50 personnes pour les réunions, vers juin 2020, les activités du campus ont repris avec une participation d'environ 15 à 20 étudiants, principalement des amis et quelques membres de l'Église, notamment du pieu de Makélékélé.

							En raison de la fermeture des écoles, de nombreux membres de l'Église n'ont pas pu participer aux activités du campus.
						 
